const { DataTypes } = require("sequelize");
const db = require("./db");

const Owner = db.define("owner", {
  name: {
    type: DataTypes.STRING,
    allowNull: false,
  },
  email: {
    type: DataTypes.STRING,
    allowNull: false,
    unique: true,
    validate: {
      isEmail: true,
    },
  },
});

module.exports = Owner;
